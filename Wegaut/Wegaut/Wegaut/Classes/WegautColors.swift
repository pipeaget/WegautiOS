import UIKit

extension UIColor {
    
    class var deepPurple: UIColor {
        
        return UIColor(red: 84/255,
                       green: 25/255,
                       blue: 127/255,
                       alpha: 1)
    }
    
    class var mediumPurple: UIColor {
        
        return UIColor(red: 144/255,
                       green: 19/255,
                       blue: 254/255,
                       alpha: 1)
    }
    
    class var blackPurple: UIColor {
        
        return UIColor(red: 64/255,
                       green: 59/255,
                       blue: 69/255,
                       alpha: 1)
    }
}
